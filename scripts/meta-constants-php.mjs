/**
 * PHP source for MetaConstants (from default inventory meta). Shared by build.js and verify-meta-constants.mjs.
 *
 * @param {{ dataset_version: string, policy_id: string, schema_version: string, profile_id?: string }} meta
 */
export function renderMetaConstantsPhp(meta) {
  const ds = phpSingleQuoted(meta.dataset_version);
  const pid = phpSingleQuoted(meta.policy_id);
  const sv = phpSingleQuoted(meta.schema_version);
  const prof = meta.profile_id != null ? phpSingleQuoted(meta.profile_id) : null;
  const profileConst =
    prof != null
      ? `
    public const PROFILE_ID = ${prof};
`
      : "";
  return `<?php

declare(strict_types=1);

namespace JoshDaugherty\\IpaUnicodeInventory;

/**
 * Values from data/inventory.json → meta (corpus_inclusive default bundle). Generated by scripts/build.js — do not edit.
 */
final class MetaConstants
{
    public const DATASET_VERSION = ${ds};

    public const POLICY_ID = ${pid};
${profileConst}
    public const SCHEMA_VERSION = ${sv};
}
`;
}

/** PHP single-quoted string literal (escaped for use as '...'). */
function phpSingleQuoted(s) {
  return "'" + String(s).replace(/\\/g, "\\\\").replace(/'/g, "\\'") + "'";
}
